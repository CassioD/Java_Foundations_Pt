public class Countdown_Soln {

    public static void main(String[] args) {
        
//Count up from 0 to 5
	for(int i = 0; i <= 5; i=i+1) {
            System.out.print(i +" "); 
        }        

//Cound all even numbers from 0 to 20
	for(int i = 0; i <= 20; i=i+2) {
            System.out.print(i +" "); 
        }
        
    }
}
